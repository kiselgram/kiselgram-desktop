    public EmojiPicker(Consumer<String> onSelect) {
        setAutoHide(true);
        setAutoFix(true);

        var grid = new GridPane();
        grid.setStyle("-fx-background-color: #242f3d; -fx-background-radius: 8; -fx-padding: 8;");
        grid.setHgap(2);
        grid.setVgap(2);

        int cols = 8;
        int row = 0, col = 0;
        for (var emoji : EMOJIS) {
            var btn = new Button(emoji);
            btn.setStyle("-fx-background-color: transparent; -fx-font-size: 20px; -fx-cursor: hand; -fx-padding: 2; -fx-min-width: 36; -fx-min-height: 36; -fx-border-radius: 4;");
            btn.setOnMouseEntered(e -> btn.setStyle("-fx-background-color: #2b5278; -fx-font-size: 20px; -fx-cursor: hand; -fx-padding: 2; -fx-min-width: 36; -fx-min-height: 36; -fx-border-radius: 4;"));
            btn.setOnMouseExited(e -> btn.setStyle("-fx-background-color: transparent; -fx-font-size: 20px; -fx-cursor: hand; -fx-padding: 2; -fx-min-width: 36; -fx-min-height: 36; -fx-border-radius: 4;"));
            btn.setOnAction(e -> {
                onSelect.accept(emoji);
                hide();
            });
            grid.add(btn, col, row);
            col++;
            if (col >= cols) { col = 0; row++; }
        }

        var scroll = new ScrollPane(grid);
        scroll.setStyle("-fx-background: #242f3d; -fx-background-color: #242f3d;");
        scroll.setPrefSize(360, 280);
        scroll.setFitToWidth(true);
        scroll.setVbarPolicy(ScrollPane.ScrollBarPolicy.AS_NEEDED);

        getScene().setRoot(scroll);
    }

    public void show(Node anchor) {
        var anchorBounds = anchor.localToScene(anchor.getBoundsInLocal());
        var window = anchor.getScene().getWindow();
        show(anchor, window.getX() + anchorBounds.getMinX(),
                window.getY() + anchorBounds.getMinY() - 300);
    }
